// to call function
void defaultBootLoader(){
	// Pin assignments
	//turn all PORTB/PORTC/PORTD pins as input(ELECTRICALLY SAFFEST OPTIONS AT START) 
	PORTB &= 0b00000000;
	PORTC &= 0b00000000;
	PORTD &= 0b00000000;
	DDRB &= 0b00000000;
	DDRC &= 0b00000000;
	DDRD &= 0b00000000;
	//GLOBAL INTERRUPT ENABLE 
	SREG |= ( 1 << SREG_I);

		// DEFAULT
		//BUTTON PIN GPIO13 (PORTB5) INPUT PULLUP 
		//POT gpio 14 (PORTC0) INPUT

	//PORTB settings
	PORTB |= (1 << PORTB5);
	DDRB &= ~( 1 << DDB5);

	//PORTC settings
	PORTC &= ~( 1 << PORTC0);
	DDRC |= (1 << DDC0);

		// timerCounter CLOCK 2 setting and prescaller
	TCCR2A &= 0b00000000;
	TCCR1B |= (1 << CS20);

		// timerCounter CLOCK 1 setting and prescaller
	TCCR1A &= 0b00000000;
	TCCR1B |= (1 << CS10);

		// ADC SETTINGS
	ADMUX &= 0b00000000;
	ADCSRA |= (1<<ADEN) | (1<<ADPS2) | (1<<ADPS1) | (1<<ADPS0);



	//ENABLE PINS SPECIFIC INTERRUPTS
	PCICR |= ( 1 << PCIE0);
	PCMSK0 |= ( 1 << PCINT5);


    //INITIALIZE CLOCK 1 COUNTING
	clk1CountMillis(START);
	//INITIALIZE SERIAL 
	Serial.begin(9600);
	//INITIALIZE SCREEN 
	screenInitialize();
	//LOADING SCREEN
	Screen.displayFunctionGroup(0,7,loadingScreen1);
	_delay_ms(2000);
	Screen.wholeScreenClearDisplay();
}



